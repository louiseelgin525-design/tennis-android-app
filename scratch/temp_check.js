        function calculatePlayerOfTheWeek() {
            let sevenDaysAgo = Date.now() - 7 * 24 * 60 * 60 * 1000;
            let growthMap = {};
            let tourneyCount = {};

            // 1. Try 7 days
            let recentEvents = clubData.history.filter(e => e.id >= sevenDaysAgo);
            if (recentEvents.length === 0) {
                // Fallback to 30 days
                let thirtyDaysAgo = Date.now() - 30 * 24 * 60 * 60 * 1000;
                recentEvents = clubData.history.filter(e => e.id >= thirtyDaysAgo);
            }
            if (recentEvents.length === 0 && clubData.history.length > 0) {
                // Fallback to the most recent single tournament
                const sortedByDate = [...clubData.history].sort((a, b) => b.id - a.id);
                recentEvents = [sortedByDate[0]];
            }

            if (recentEvents.length === 0) {
                return null; // No history at all
            }

            // Calculate stats on the selected events
            recentEvents.forEach(entry => {
                const ratings = entry.structuredRatings ? entry.structuredRatings.split(";;") : [];
                ratings.forEach(rStr => {
                    const p = rStr.split("::");
                    if (p.length >= 4) {
                        const name = decodeName(p[0]).toLowerCase();
                        const delta = parseFloat(p[3] || "0");
                        growthMap[name] = (growthMap[name] || 0) + delta;
                        tourneyCount[name] = (tourneyCount[name] || 0) + 1;
                    }
                });
            });

            // Find the best player
            let bestName = "";
            let maxGain = -999;
            Object.keys(growthMap).forEach(name => {
                if (growthMap[name] > maxGain) {
                    maxGain = growthMap[name];
                    bestName = name;
                }
            });

            // If the highest gain is <= 0, we can just find the player with the highest rating delta in the last active tournament
            if (!bestName || maxGain <= 0) {
                // Fallback to highest positive rating change in history
                let highestDelta = 0;
                let highestName = "";
                clubData.history.forEach(entry => {
                    const ratings = entry.structuredRatings ? entry.structuredRatings.split(";;") : [];
                    ratings.forEach(rStr => {
                        const p = rStr.split("::");
                        if (p.length >= 4) {
                            const name = decodeName(p[0]).toLowerCase();
                            const delta = parseFloat(p[3] || "0");
                            if (delta > highestDelta) {
                                highestDelta = delta;
                                highestName = name;
                            }
                        }
                    });
                });
                if (highestName) {
                    bestName = highestName;
                    maxGain = highestDelta;
                    tourneyCount[bestName] = 1;
                }
            }

            if (!bestName) return null;

            // Resolve back to the actual player object in clubData.players
            const playerObj = clubData.players.find(p => p.fullName.toLowerCase() === bestName);
            if (!playerObj) return null;

            // Now let's calculate match win streak (overall consecutive match wins from chronological match history)
            let currentStreak = 0;
            const sortedHistory = [...clubData.history].sort((a, b) => a.id - b.id);
            
            // Loop through history to build streak
            sortedHistory.forEach(entry => {
                const matches = entry.structuredMatches ? entry.structuredMatches.split(";;") : [];
                matches.forEach(matchStr => {
                    const p = matchStr.split("::");
                    if (p.length >= 3) {
                        const p1 = decodeName(p[0]).toLowerCase();
                        const p2 = decodeName(p[1]).toLowerCase();
                        const score = p[2];

                        const scoreParts = score.split(":");
                        let p1Won = false;
                        if (score === 'W') p1Won = true;
                        else if (score === 'L') p1Won = false;
                        else if (scoreParts.length === 2) {
                            p1Won = parseInt(scoreParts[0]) > parseInt(scoreParts[1]);
                        }

                        if (p1 === bestName) {
                            if (p1Won) {
                                currentStreak++;
                            } else {
                                currentStreak = 0;
                            }
                        } else if (p2 === bestName) {
                            if (!p1Won) {
                                currentStreak++;
                            } else {
                                currentStreak = 0;
                            }
                        }
                    }
                });
            });

            return {
                player: playerObj,
                gain: maxGain,
                tournaments: tourneyCount[bestName] || 0,
                streak: currentStreak
            };
        }

        function calculateTopRecords() {
            const records = {
                titled: { player: null, value: "" },
                growth: { player: null, value: "" },
                streak: { player: null, value: "" },
                stable: { player: null, value: "" }
            };

            if (clubData.players.length === 0) return records;

            // 1. Titled (Most gold medals)
            let maxGold = -1;
            let bestTitled = null;
            clubData.players.forEach(p => {
                if (p.goldMedals > maxGold) {
                    maxGold = p.goldMedals;
                    bestTitled = p;
                } else if (p.goldMedals === maxGold && bestTitled && p.rating > bestTitled.rating) {
                    bestTitled = p;
                }
            });
            if (bestTitled && maxGold > 0) {
                records.titled = { player: bestTitled, value: `${maxGold} золотых` };
            }

            // 2. Growth (Highest rating change in last 30 days)
            const thirtyDaysAgo = Date.now() - 30 * 24 * 60 * 60 * 1000;
            const growthMap = {};
            clubData.history.forEach(entry => {
                if (entry.id >= thirtyDaysAgo) {
                    const ratings = entry.structuredRatings ? entry.structuredRatings.split(";;") : [];
                    ratings.forEach(rStr => {
                        const p = rStr.split("::");
                        if (p.length >= 4) {
                            const name = decodeName(p[0]).toLowerCase();
                            const delta = parseFloat(p[3] || "0");
                            growthMap[name] = (growthMap[name] || 0) + delta;
                        }
                    });
                }
            });
            let maxGrowth = 0;
            let bestGrowthName = "";
            Object.keys(growthMap).forEach(name => {
                if (growthMap[name] > maxGrowth) {
                    maxGrowth = growthMap[name];
                    bestGrowthName = name;
                }
            });
            if (bestGrowthName && maxGrowth > 0) {
                const pObj = clubData.players.find(p => p.fullName.toLowerCase() === bestGrowthName);
                if (pObj) {
                    records.growth = { player: pObj, value: `+${maxGrowth.toFixed(1)} за месяц` };
                }
            }

            // 3. Best Win Streak (Longest consecutive match win streak in chronological history)
            const playerStreaks = {}; // { nameLower: { current: 0, max: 0 } }
            clubData.players.forEach(p => {
                playerStreaks[p.fullName.toLowerCase()] = { current: 0, max: 0 };
            });

            const sortedHistory = [...clubData.history].sort((a, b) => a.id - b.id);
            sortedHistory.forEach(entry => {
                const matches = entry.structuredMatches ? entry.structuredMatches.split(";;") : [];
                matches.forEach(matchStr => {
                    const p = matchStr.split("::");
                    if (p.length >= 3) {
                        const p1 = decodeName(p[0]).toLowerCase();
                        const p2 = decodeName(p[1]).toLowerCase();
                        const score = p[2];

                        const scoreParts = score.split(":");
                        let p1Won = false;
                        if (score === 'W') p1Won = true;
                        else if (score === 'L') p1Won = false;
                        else if (scoreParts.length === 2) {
                            p1Won = parseInt(scoreParts[0]) > parseInt(scoreParts[1]);
                        }

                        // Update streak for p1
                        if (playerStreaks[p1]) {
                            if (p1Won) {
                                playerStreaks[p1].current++;
                                playerStreaks[p1].max = Math.max(playerStreaks[p1].max, playerStreaks[p1].current);
                            } else {
                                playerStreaks[p1].current = 0;
                            }
                        }
                        // Update streak for p2
                        if (playerStreaks[p2]) {
                            if (!p1Won) {
                                playerStreaks[p2].current++;
                                playerStreaks[p2].max = Math.max(playerStreaks[p2].max, playerStreaks[p2].current);
                            } else {
                                playerStreaks[p2].current = 0;
                            }
                        }
                    }
                });
            });

            let maxStreak = 0;
            let bestStreakName = "";
            Object.keys(playerStreaks).forEach(name => {
                if (playerStreaks[name].max > maxStreak) {
                    maxStreak = playerStreaks[name].max;
                    bestStreakName = name;
                }
            });
            if (bestStreakName && maxStreak > 0) {
                const pObj = clubData.players.find(p => p.fullName.toLowerCase() === bestStreakName);
                if (pObj) {
                    records.streak = { player: pObj, value: formatPlural(maxStreak, 'победа', 'победы', 'побед') };
                }
            }

            // 4. Stable (Highest win rate, min 5 matches)
            let bestStable = null;
            let maxWinRate = -1;
            clubData.players.forEach(p => {
                const totalMatches = p.wins + p.losses;
                if (totalMatches >= 5) {
                    const rate = p.wins / totalMatches;
                    if (rate > maxWinRate) {
                        maxWinRate = rate;
                        bestStable = p;
                    } else if (rate === maxWinRate && bestStable && p.rating > bestStable.rating) {
                        bestStable = p;
                    }
                }
            });
            if (bestStable && maxWinRate > 0) {
                records.stable = { player: bestStable, value: `${Math.round(maxWinRate * 100)}% побед` };
            }

            return records;
        }

