        function getPlayoffParticipant(matchId, slot, scores, names) {
            if (matchId === 5) {
                const prevId = slot === 1 ? 1 : 2;
                return getWinnerOfMatch(prevId, scores, names) || "?";
            }
            if (matchId === 6) {
                const prevId = slot === 1 ? 3 : 4;
                return getWinnerOfMatch(prevId, scores, names) || "?";
            }
            if (matchId === 7) {
                const prevId = slot === 1 ? 5 : 6;
                return getWinnerOfMatch(prevId, scores, names) || "?";
            }
            // Match 1, 2, 3, 4 quarterfinals are mapped to index 0..7
            if (matchId === 1) return slot === 1 ? names[0] : names[1];
            if (matchId === 2) return slot === 1 ? names[2] : names[3];
            if (matchId === 3) return slot === 1 ? names[4] : names[5];
            if (matchId === 4) return slot === 1 ? names[6] : names[7];
            return "?";
        }

        function getWinnerOfMatch(id, scores, names) {
            const score = scores[id];
            if (!score || !score.includes(":")) return null;
            const parts = score.split(":");
            const s1 = parseInt(parts[0]);
            const s2 = parseInt(parts[1]);
            
            if (id === 1) return s1 > s2 ? names[0] : names[1];
            if (id === 2) return s1 > s2 ? names[2] : names[3];
            if (id === 3) return s1 > s2 ? names[4] : names[5];
            if (id === 4) return s1 > s2 ? names[6] : names[7];
            
            if (id === 5) return s1 > s2 ? getWinnerOfMatch(1, scores, names) : getWinnerOfMatch(2, scores, names);
            if (id === 6) return s1 > s2 ? getWinnerOfMatch(3, scores, names) : getWinnerOfMatch(4, scores, names);
            return null;
        }

        function calculateRoundRobinStandings(playerCount, scores) {
            const wins = new Array(playerCount).fill(0);
            
            for (let f = 0; f < playerCount; f++) {
                for (let s = f + 1; s < playerCount; s++) {
                    const scoreKey = `${f}_${s}`;
                    const score = scores[scoreKey];
                    if (score) {
                        if (score === 'W') {
                            wins[f]++;
                        } else if (score === 'L') {
                            wins[s]++;
                        } else if (score.includes(":")) {
                            const parts = score.split(":");
                            const p1 = parseInt(parts[0]);
                            const p2 = parseInt(parts[1]);
                            if (p1 > p2) wins[f]++;
                            else if (p2 > p1) wins[s]++;
                        }
                    }
                }
            }

            const list = [];
            for (let i = 0; i < playerCount; i++) {
                list.push({ index: i, points: wins[i] });
            }

            // Sort by points descending
            list.sort((a, b) => b.points - a.points);
            return list;
        }

