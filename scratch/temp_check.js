        // RTTF RATING CALCULATION LOGIC (PORT FROM KOTLIN)
        function calculateDeltas(playerNames, scores, K, applyPlaceBonuses, playersList) {
            const tournamentPlayers = playerNames.map(name => {
                const p = playersList.find(pl => pl.fullName.toLowerCase() === name.toLowerCase());
                return p ? { ...p } : { id: 999, fullName: name, rating: 200.0, tournamentsPlayed: 0, wins: 0, losses: 0 };
            });

            const accumulatedDeltas = {};
            const winIncrements = {};
            const lossIncrements = {};
            tournamentPlayers.forEach(p => {
                accumulatedDeltas[p.fullName.toLowerCase()] = 0.0;
                winIncrements[p.fullName.toLowerCase()] = 0;
                lossIncrements[p.fullName.toLowerCase()] = 0;
            });

            const parseScore = (str) => {
                if (!str || !str.includes(":")) return null;
                const p = str.split(":");
                return [parseInt(p[0]), parseInt(p[1])];
            };

            for (let f = 0; f < playerNames.length; f++) {
                for (let s = f + 1; s < playerNames.length; s++) {
                    const scoreKey = `${f}_${s}`;
                    const scoreStr = scores[scoreKey];
                    const score = parseScore(scoreStr);
                    if (!score || score[0] === score[1]) continue;

                    const p1 = tournamentPlayers[f];
                    const p2 = tournamentPlayers[s];

                    const p1Won = score[0] > score[1];
                    const winner = p1Won ? p1 : p2;
                    const loser = p1Won ? p2 : p1;
                    const winnerScore = p1Won ? score[0] : score[1];
                    const loserScore = p1Won ? score[1] : score[0];

                    const winnerName = winner.fullName.toLowerCase();
                    const loserName = loser.fullName.toLowerCase();

                    winIncrements[winnerName]++;
                    lossIncrements[loserName]++;

                    // rttfBaseDelta
                    const diff = winner.rating - loser.rating;
                    let baseDelta = 0.0;
                    if (diff < 100.0) {
                        baseDelta = (100.0 - diff) / 10.0;
                    }

                    if (baseDelta <= 0.0) continue;

                    // scoreDominanceMultiplier
                    let scoreCoeff = 1.0;
                    if (winnerScore === 2 && loserScore === 1) scoreCoeff = 0.8;
                    else if (winnerScore === 3 && loserScore === 2) scoreCoeff = 0.8;
                    else if (winnerScore === 3 && loserScore === 1) scoreCoeff = 0.9;
                    else if (winnerScore === 1 && loserScore === 0) scoreCoeff = 1.0;

                    const winnerIsNovice = winner.tournamentsPlayed <= 5;
                    const loserIsNovice = loser.tournamentsPlayed <= 5;

                    const winnerDelta = winnerIsNovice ? Math.round(baseDelta * 1.0 * 100) / 100 
                                                       : Math.round(baseDelta * K * scoreCoeff * 100) / 100;
                    const loserDelta = loserIsNovice ? Math.round(baseDelta * 0.5 * 100) / 100 
                                                     : Math.round(baseDelta * K * scoreCoeff * 100) / 100;

                    accumulatedDeltas[winnerName] += winnerDelta;
                    accumulatedDeltas[loserName] -= loserDelta;
                }
            }

            // Standings
            const standings = calculateRoundRobinStandings(playerNames.length, scores);
            if (applyPlaceBonuses && playerNames.length >= 3) {
                const bonus1 = 1.5;
                const bonus2 = 1.0;
                const bonus3 = 0.5;

                const p1Name = tournamentPlayers[standings[0].index].fullName.toLowerCase();
                const p2Name = tournamentPlayers[standings[1].index].fullName.toLowerCase();
                const p3Name = tournamentPlayers[standings[2].index].fullName.toLowerCase();

                accumulatedDeltas[p1Name] += bonus1;
                accumulatedDeltas[p2Name] += bonus2;
                accumulatedDeltas[p3Name] += bonus3;
            }

            return {
                deltas: accumulatedDeltas,
                wins: winIncrements,
                losses: lossIncrements,
                standings: standings
            };
        }

