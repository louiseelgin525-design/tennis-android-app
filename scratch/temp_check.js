        // RENDER READ-ONLY AND ADMIN PLAYERS LIST
function getPlayerLastDelta(player) {
            let lastDelta = 0;
            const sortedHistory = [...clubData.history].sort((a, b) => b.id - a.id);
            for (const entry of sortedHistory) {
                const ratings = entry.structuredRatings ? entry.structuredRatings.split(";;") : [];
                let found = false;
                for (const rStr of ratings) {
                    const p = rStr.split("::");
                    if (p.length >= 4) {
                        const name = decodeName(p[0]);
                        if (name.toLowerCase() === player.fullName.toLowerCase()) {
                            lastDelta = parseFloat(p[3]);
                            found = true;
                            break;
                        }
                    }
                }
                if (found) break;
            }
            return lastDelta;
        }

