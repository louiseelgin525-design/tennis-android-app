        // CALCULATE ACHIEVEMENTS FOR HALL OF FAME
        function calculateAchievements() {
            const achievements = {}; // { playerFullNameLower: { badgeName: count } }

            // Initialize structure for all players
            clubData.players.forEach(p => {
                achievements[p.fullName.toLowerCase()] = {};
            });

            // 1. Серийный чемпион (Serial Champion)
            const sortedHistory = [...clubData.history].sort((a, b) => a.id - b.id);
            let currentWinner = "";
            let streakCount = 0;
            sortedHistory.forEach(entry => {
                const ratings = entry.structuredRatings ? entry.structuredRatings.split(";;") : [];
                if (ratings.length > 0) {
                    const p0 = ratings[0].split("::");
                    if (p0.length >= 1) {
                        const winnerName = decodeName(p0[0]).toLowerCase();
                        if (winnerName === currentWinner) {
                            streakCount++;
                            if (streakCount === 3) {
                                if (achievements[winnerName]) {
                                    achievements[winnerName]['champion'] = (achievements[winnerName]['champion'] || 0) + 1;
                                }
                                streakCount = 0;
                            }
                        } else {
                            currentWinner = winnerName;
                            streakCount = 1;
                        }
                    }
                }
            });

            // 2. Гроза авторитетов (Giant Slayer)
            // 5. Неуязвимый (Invincible)
            const giantSlayers = {};
            const invincibles = {};
            
            clubData.history.forEach(entry => {
                const ratings = entry.structuredRatings ? entry.structuredRatings.split(";;") : [];
                const ratingsMap = {};
                ratings.forEach(rStr => {
                    const p = rStr.split("::");
                    if (p.length >= 2) {
                        ratingsMap[decodeName(p[0]).toLowerCase()] = parseFloat(p[1]);
                    }
                });

                const tourneyStats = {};
                const matches = entry.structuredMatches ? entry.structuredMatches.split(";;") : [];
                matches.forEach(matchStr => {
                    const p = matchStr.split("::");
                    if (p.length >= 3) {
                        const p1 = decodeName(p[0]).toLowerCase();
                        const p2 = decodeName(p[1]).toLowerCase();
                        const score = p[2];
                        const r1 = ratingsMap[p1];
                        const r2 = ratingsMap[p2];

                        if (!tourneyStats[p1]) tourneyStats[p1] = { played: 0, wins: 0 };
                        if (!tourneyStats[p2]) tourneyStats[p2] = { played: 0, wins: 0 };

                        tourneyStats[p1].played++;
                        tourneyStats[p2].played++;

                        const scoreParts = score.split(":");
                        let p1Won = false;
                        if (score === 'W') p1Won = true;
                        else if (score === 'L') p1Won = false;
                        else if (scoreParts.length === 2) {
                            p1Won = parseInt(scoreParts[0]) > parseInt(scoreParts[1]);
                        }

                        if (p1Won) {
                            tourneyStats[p1].wins++;
                            if (r1 !== undefined && r2 !== undefined && (r2 - r1 >= 100)) {
                                giantSlayers[p1] = (giantSlayers[p1] || 0) + 1;
                            }
                        } else {
                            tourneyStats[p2].wins++;
                            if (r1 !== undefined && r2 !== undefined && (r1 - r2 >= 100)) {
                                giantSlayers[p2] = (giantSlayers[p2] || 0) + 1;
                            }
                        }
                    }
                });

                // Compute invincibles for this tourney
                Object.keys(tourneyStats).forEach(name => {
                    const stats = tourneyStats[name];
                    if (stats.played >= 2 && stats.wins === stats.played) {
                        invincibles[name] = (invincibles[name] || 0) + 1;
                    }
                });
            });

            // Map giantSlayers and invincibles
            Object.keys(giantSlayers).forEach(name => {
                if (achievements[name]) achievements[name]['slayer'] = giantSlayers[name];
            });
            Object.keys(invincibles).forEach(name => {
                if (achievements[name]) achievements[name]['invincible'] = invincibles[name];
            });

            // 3. Взрывной рост (Explosive Growth)
            const growthMap = {};
            const thirtyDaysAgo = Date.now() - 30 * 24 * 60 * 60 * 1000;
            clubData.history.forEach(entry => {
                if (entry.id >= thirtyDaysAgo) {
                    const ratings = entry.structuredRatings ? entry.structuredRatings.split(";;") : [];
                    ratings.forEach(rStr => {
                        const p = rStr.split("::");
                        if (p.length >= 4) {
                            const name = decodeName(p[0]).toLowerCase();
                            const delta = parseFloat(p[3] || "0");
                            growthMap[name] = (growthMap[name] || 0) + delta;
                        }
                    });
                }
            });
            Object.keys(growthMap).forEach(name => {
                if (growthMap[name] >= 50 && achievements[name]) {
                    achievements[name]['growth'] = 1;
                }
            });

            // 4. Железный человек (Iron Man)
            // 6. Гроссмейстер (Grandmaster)
            clubData.players.forEach(p => {
                const name = p.fullName.toLowerCase();
                if (!achievements[name]) achievements[name] = {};

                if (p.tournamentsPlayed >= 100) {
                    achievements[name]['iron'] = 2;
                } else if (p.tournamentsPlayed >= 50) {
                    achievements[name]['iron'] = 1;
                }

                if (p.rating >= 350.0) {
                    achievements[name]['grandmaster'] = 1;
                }
            });

            return achievements;
        }

