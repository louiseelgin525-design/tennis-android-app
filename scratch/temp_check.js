        // LOGIN ATTEMPT
        function attemptLogin(clubId, pin) {
            debugLog("attemptLogin запущена для ID: '" + clubId + "'");
            if (!db) {
                showLoader(false);
                showToast("Ошибка: База данных не инициализирована!");
                debugLog("Ошибка: db === null. База не готова.");
                return;
            }
            showLoader(true);
            try {
                debugLog("Отправка запроса проверки существования clubConfigs/" + clubId + "...");
                // Validate clubConfig exists (using .child() for unicode key safety)
                db.ref("clubConfigs").child(clubId).once('value', (snapshot) => {
                    debugLog("Ответ от Firebase получен.");
                    if (snapshot.exists()) {
                        const config = JSON.parse(snapshot.val());
                        
                        if (selectedRole === 'admin') {
                            if (config.adminPin === pin) {
                                debugLog("Администратор успешно авторизован по ПИН-коду.");
                                isAdmin = true;
                                safeStorage.setItem('tennis_admin_mode', 'true');
                                safeStorage.setItem('tennis_admin_pin', pin);
                            } else {
                                debugLog("Ошибка: Неверный ПИН-код администратора.");
                                showLoader(false);
                                showToast("Неверный ПИН-код администратора!");
                                return;
                            }
                        } else {
                            isAdmin = false;
                            safeStorage.setItem('tennis_admin_mode', 'false');
                        }

                        // Save to local storage
                        safeStorage.setItem('tennis_club_id', clubId);
                        activeClubId = clubId;
                        
                        // Set header
                        const displayClubName = config.displayName || (clubId.charAt(0).toUpperCase() + clubId.slice(1));
                        activeClubDisplayName = displayClubName;
                        headerClubName.innerHTML = `${displayClubName} ${isAdmin ? '<span class="admin-badge">' + SVG_ICONS.crown + ' Админ</span>' : ''}`;
                        
                        const roleEl = document.getElementById('header-club-role');
                        if (roleEl) {
                            roleEl.innerHTML = isAdmin ? SVG_ICONS.crown + ' Режим организатора (iOS)' : SVG_ICONS.eye + ' Режим просмотра (iOS)';
                        }
                        
                        const analyticsBtn = document.getElementById('analytics-header-btn');
                        if (analyticsBtn) analyticsBtn.style.display = isAdmin ? 'block' : 'none';
                        
                        // Load and listen to data
                        initDatabaseListeners(clubId);
                        logAnalyticsEvent('load');
                    } else {
                        debugLog("Ошибка: Клуб '" + clubId + "' не найден в db path 'clubConfigs'.");
                        showLoader(false);
                        showToast("Клуб не найден в базе данных!");
                        safeStorage.removeItem('tennis_club_id');
                    }
                }, (error) => {
                    debugLog("Firebase error: " + error.message);
                    showLoader(false);
                    showToast("Ошибка соединения с интернетом!");
                });
            } catch (err) {
                debugLog("Исключение в attemptLogin: " + err.message);
                showLoader(false);
                showToast("Ошибка запуска: " + err.message);
            }
        }

        // INITIALIZE REAL-TIME LISTENERS
        function initDatabaseListeners(clubId) {
            // 1. Players Listener
            db.ref(`clubs/${clubId}/players`).on('value', (snapshot) => {
                const rawJson = snapshot.val();
                let list = [];
                if (rawJson) {
                    try {
                        list = JSON.parse(rawJson);
                    } catch (e) {
                        list = [];
                    }
                }
                clubData.players = list;
                renderPlayers();
                renderHall();
                updateActiveTournament(); // refresh table names if active
            });

            // 2. History Listener
            db.ref(`clubs/${clubId}/history`).on('value', (snapshot) => {
                const rawJson = snapshot.val();
                let list = [];
                if (rawJson) {
                    if (typeof rawJson === 'string') {
                        try {
                            list = JSON.parse(rawJson);
                        } catch (e) {
                            list = [];
                        }
                    } else if (Array.isArray(rawJson)) {
                        list = rawJson;
                    } else if (typeof rawJson === 'object') {
                        list = Object.values(rawJson);
                    }
                }
                clubData.history = list;
                renderHistory();
                renderHall();
            });

            // 3. Draft/Active Tournament Listener
            db.ref(`clubs/${clubId}/draft`).on('value', (snapshot) => {
                clubData.draft = snapshot.val() || null;
                updateActiveTournament();
            });

            // 4. Avatars Listener (caches player photos)
            db.ref(`clubs/${clubId}/avatars`).on('value', (snapshot) => {
                clubData.avatars = snapshot.val() || {};
                renderPlayers();
            });

            // Hide Login and Show App
            loginScreen.style.display = 'none';
            appContainer.style.display = 'flex';
            
            // Go to default tab
            switchTab('ratings');
            showLoader(false);
        }

